/*
 * 알림기.js  —  전역 window.알림기
 * 비트코인 가격알림 PWA의 "알림" 모듈
 *
 * 핵심 책임:
 *  - $300 단위 경계 돌파 감지 ($300 단위 배수 경계를 넘을 때마다 1회 알림)
 *  - 기준선 인덱스 = Math.floor(가격 / 단위). 인덱스가 직전과 달라지면 발동.
 *  - 한 번의 입력에서 여러 칸을 점프해도 최신 가격 기준으로 "1회"만 발동.
 *  - 상승/하락 방향 판정.
 *  - Notification API 권한요청/발송 래퍼 (권한 없으면 콘솔 경고만, 에러 금지).
 *
 * 순수 바닐라 JS, 의존성 0.
 * Node 환경(테스트)에서도 경계 감지 로직을 순수 함수로 호출할 수 있도록 분리했다.
 */

(function (전역) {
  'use strict';

  // ─────────────────────────────────────────────────────────────
  // 내부 상태
  // ─────────────────────────────────────────────────────────────
  var 단위 = 500;            // 알림 단위(USD). 기본 500. (비트코인 전용)
  var 직전인덱스 = null;      // 마지막으로 본 가격의 기준선 인덱스 (없으면 null = 첫 입력)
  var 앵커 = 0;              // 기준점(USD). ON 누른 순간 가격으로 설정 → 거기서 ±단위 움직이면 발동.
                            //  0이면 절대 그리드($300 배수) 기준(과거 동작/테스트 호환).

  // ─────────────────────────────────────────────────────────────
  // 순수 함수: 경계 감지 로직
  //   - 부수효과 없음(상태/Notification 사용 안 함) → Node 단위테스트 가능
  //   - 인자로 받은 직전인덱스를 기준으로 계산하고 결과를 객체로 반환
  //
  //   반환:
  //   {
  //     발동: boolean,            // 경계를 넘어 알림을 발송해야 하는가
  //     방향: "상승"|"하락"|null,  // 발동 시 방향, 미발동이면 null
  //     새인덱스: number,         // 이번 가격의 기준선 인덱스 (다음 호출의 직전인덱스로 사용)
  //     기준가: number            // 이번 가격이 속한 기준선 가격 (인덱스 * 단위)
  //   }
  // ─────────────────────────────────────────────────────────────
  function 경계감지(가격, 직전idx, 사용단위, 앵커기준) {
    var u = 사용단위 || 단위;
    var a = 앵커기준 || 0;
    // 앵커 기준으로 칸 인덱스 계산. 기준가는 다시 앵커를 더해 실제 USD 가격으로 환산.
    var 새인덱스 = Math.floor((가격 - a) / u);

    // 첫 입력: 기준선만 잡고 발동하지 않는다.
    if (직전idx === null || 직전idx === undefined) {
      return { 발동: false, 방향: null, 새인덱스: 새인덱스, 기준가: a + 새인덱스 * u };
    }

    if (새인덱스 === 직전idx) {
      // 같은 칸 안에서 움직임 → 무알림
      return { 발동: false, 방향: null, 새인덱스: 새인덱스, 기준가: a + 새인덱스 * u };
    }

    // 인덱스가 달라짐 → 여러 칸 점프여도 1회만 발동, 방향은 최신가 기준
    var 방향 = (새인덱스 > 직전idx) ? '상승' : '하락';
    return { 발동: true, 방향: 방향, 새인덱스: 새인덱스, 기준가: a + 새인덱스 * u };
  }

  // ─────────────────────────────────────────────────────────────
  // 숫자 천단위 콤마 포맷 ($67,300 형태)
  // ─────────────────────────────────────────────────────────────
  function 콤마(숫자) {
    // 소수점 이하는 버려서 정수 USD로 표기 (계약서 예시가 정수 표기)
    var 정수 = Math.round(숫자);
    return 정수.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  }

  // ─────────────────────────────────────────────────────────────
  // Notification 권한요청 래퍼  →  Promise<boolean>
  // ─────────────────────────────────────────────────────────────
  function 권한요청() {
    // Notification API가 없는 환경(구형/Node)에서는 false 반환, 에러 금지
    if (typeof Notification === 'undefined') {
      console.warn('[알림기] 이 환경은 Notification API를 지원하지 않습니다.');
      return Promise.resolve(false);
    }
    try {
      var 결과 = Notification.requestPermission();
      // 구형 브라우저는 콜백 방식 → Promise로 감싼다.
      if (결과 && typeof 결과.then === 'function') {
        return 결과.then(function (권한) {
          return 권한 === 'granted';
        });
      }
      return new Promise(function (resolve) {
        Notification.requestPermission(function (권한) {
          resolve(권한 === 'granted');
        });
      });
    } catch (e) {
      console.warn('[알림기] 권한요청 중 예외:', e);
      return Promise.resolve(false);
    }
  }

  // ─────────────────────────────────────────────────────────────
  // 실제 알림 발송 (방향, 가격, 기준가)
  //   - 권한 없으면 콘솔 경고만, 에러 던지지 않음
  // ─────────────────────────────────────────────────────────────
  function 알림발송(방향, 가격, 기준가, 이전기준가) {
    var 화살표 = (방향 === '상승') ? '📈' : '📉';
    var 제목 = '비트코인 $' + 단위 + ' 돌파 ' + 화살표;
    // 본문 예: 현재가 $67,300 (기준 $67,000 → $67,300)
    var 본문 = '현재가 $' + 콤마(가격) +
               ' (기준 $' + 콤마(이전기준가) + ' → $' + 콤마(기준가) + ')';

    if (typeof Notification === 'undefined') {
      console.warn('[알림기] Notification 미지원 — ' + 제목 + ' / ' + 본문);
      return false;
    }
    if (Notification.permission !== 'granted') {
      console.warn('[알림기] 알림 권한 없음 — ' + 제목 + ' / ' + 본문);
      return false;
    }
    try {
      new Notification(제목, { body: 본문 });
      return true;
    } catch (e) {
      console.warn('[알림기] 알림 발송 중 예외:', e);
      return false;
    }
  }

  // ─────────────────────────────────────────────────────────────
  // 공개 API
  // ─────────────────────────────────────────────────────────────
  var 알림기 = {
    // 알림 단위 설정 (기본 300). 변경 시 기준선 인덱스를 재계산하기 위해 직전인덱스를 리셋.
    설정: function (단위USD) {
      if (typeof 단위USD === 'number' && 단위USD > 0) {
        단위 = 단위USD;
        직전인덱스 = null; // 단위가 바뀌면 기준선 의미가 달라지므로 다음 입력을 첫 입력 취급
      } else {
        console.warn('[알림기] 설정: 단위는 0보다 큰 숫자여야 합니다. 현재 단위 유지:', 단위);
      }
      return 단위;
    },

    // 현재 단위 조회(편의)
    현재단위: function () {
      return 단위;
    },

    // 다음 알림 경계(상방/하방) 계산 — 화면 #다음경계 표시용 편의 함수. 앵커 반영.
    다음경계: function (가격) {
      if (typeof 가격 !== 'number' || isNaN(가격)) return null;
      var idx = Math.floor((가격 - 앵커) / 단위);
      return {
        상방: 앵커 + (idx + 1) * 단위,
        하방: 앵커 + idx * 단위
      };
    },

    // 기준점(앵커) 설정 — ON 누른 순간 가격을 넣으면 여기서부터 ±단위 움직임 감시.
    //  다음 입력을 '첫 입력'으로 취급하도록 직전인덱스를 리셋한다.
    앵커설정: function (가격) {
      앵커 = (typeof 가격 === 'number' && isFinite(가격)) ? 가격 : 0;
      직전인덱스 = null;
      return 앵커;
    },

    // 현재 앵커 조회(편의)
    현재앵커: function () {
      return 앵커;
    },

    권한요청: 권한요청,

    // 가격을 넣으면 경계 비교 후 필요 시 알림 발송
    가격입력: function (가격) {
      if (typeof 가격 !== 'number' || isNaN(가격)) {
        console.warn('[알림기] 가격입력: 유효한 숫자가 아닙니다:', 가격);
        return;
      }
      var 이전인덱스 = 직전인덱스;
      var r = 경계감지(가격, 직전인덱스, 단위, 앵커);
      직전인덱스 = r.새인덱스; // 상태 갱신(발동 여부와 무관하게 항상 최신으로)
      if (r.발동) {
        var 이전기준가 = 앵커 + 이전인덱스 * 단위;
        알림발송(r.방향, 가격, r.기준가, 이전기준가);
      }
    },

    // 테스트용 강제발송 (권한/경계 무시, 알림 표시 동작만 확인)
    _강제발송: function (방향, 가격) {
      var idx = Math.floor(가격 / 단위);
      var 기준가 = idx * 단위;
      // 이전기준가는 방향에 맞춰 한 칸 차이로 가정
      var 이전기준가 = (방향 === '상승') ? (기준가 - 단위) : (기준가 + 단위);
      return 알림발송(방향, 가격, 기준가, 이전기준가);
    },

    // 내부 상태 리셋(테스트/단위변경 외 용도)
    _리셋: function () {
      직전인덱스 = null;
    },

    // 순수 경계 감지 함수 노출 (Node 단위테스트용)
    _경계감지: 경계감지
  };

  // 전역 등록 (브라우저: window, Node: 모듈 export 둘 다 지원)
  전역.알림기 = 알림기;
  if (typeof module !== 'undefined' && module.exports) {
    module.exports = 알림기;
  }
})(typeof window !== 'undefined' ? window : globalThis);
